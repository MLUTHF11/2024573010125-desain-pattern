package Bagian_2;

public class Motor {

    // atribut private
    private String merk;
    private int Tahun;

    //getter dan setter
    public String getMerk() {
        return merk;
    }

    public void setMerk(String merk) {
        this.merk = merk;
    }

    public int getTahun() {
        return Tahun;
    }

    public void setTahun(int tahun) {
        Tahun = tahun;
    }
}
